#include <bits/stdc++.h>
#define base1 311
#define base2 997
#define int long long
using namespace std;

const int mod1 = 1e9 + 7;
const int mod2 = 1e9 + 9;

string s;
int n;

pair<int, int> getHash(int l, int r, int Hash1[], int Hash2[], int p1[], int p2[]){
    int h1 = (Hash1[r] - Hash1[l - 1] * p1[r - l + 1] + mod1 * mod1) % mod1;
    int h2 = (Hash2[r] - Hash2[l - 1] * p2[r - l + 1] + mod2 * mod2) % mod2;
    return {h1, h2};
}

main(){
    cin >> s;
    n = s.size();
    s = " " + s;
    if(n == 1) return 0;
    int Hash1[n + 1] = {0}, Hash2[n + 1] = {0}, p1[n + 1] = {1}, p2[n + 1] = {1};
    for(int i = 1; i <= n; i++){
        Hash1[i] = (Hash1[i - 1] * base1 + (int)s[i]) % mod1;
        Hash2[i] = (Hash2[i - 1] * base2 + (int)s[i]) % mod2;
        p1[i] = p1[i - 1] * base1 % mod1;
        p2[i] = p2[i - 1] * base2 % mod2;
    }
    for(int len = 1; len <= n - 1; len++){
        auto x1 = getHash(1, len, Hash1, Hash2, p1, p2);
        auto x2 = getHash(n - len + 1, n, Hash1, Hash2, p1, p2);
        if(x1 == x2){
            cout << len << " ";
        }
    }
}

