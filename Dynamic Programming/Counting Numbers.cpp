#include <bits/stdc++.h>
#define endl "\n"
#define int long long
#define double long double
#define TranHungss() signed main()
#define FastIO(); ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
using namespace std;

int dp[20][2][2][11];

int solve(int pos, int tight, int started, int prev, string &s){
    if(pos == s.size()){
        return 1;
    }
    if(dp[pos][tight][started][prev] != -1){
        return dp[pos][tight][started][prev];
    }
    int res = 0, limit = tight ? s[pos] - '0' : 9;
    for(int d = 0; d <= limit; d++){
        if(started || d != 0){
            if(d == prev) continue;
            res += solve(pos + 1, tight && d == limit, started || d != 0, d, s);
        }
        else{
            res += solve(pos + 1, tight && d == limit, started || d != 0, d, s);
        }
    }
    return dp[pos][tight][started][prev] = res;
}

int f(int n){
    memset(dp, -1, sizeof(dp));
    string s = to_string(n);
    return solve(0, 1, 0, 10, s);
}

