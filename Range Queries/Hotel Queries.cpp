using namespace std;

const int inf = (int)1e18;
const int maxn = 200002;

int n, m, a[maxn], IT[4 * maxn];

void build(int i, int l, int r){
    if(l == r){
        IT[i] = a[l];
    }
    else{
        int m = (l + r) >> 1;
        build(2 * i, l, m);
        build(2 * i + 1, m + 1, r);
        IT[i] = max(IT[2 * i], IT[2 * i + 1]);
    }
}

void update(int i, int l, int r, int pos, int val){
    if(l == r){
        IT[i] -= val;
    }
    else{
        int m = (l + r) >> 1;
        if(pos <= m){
            update(2 * i, l, m, pos, val);
        }
        else{
            update(2 * i + 1, m + 1, r, pos, val);
        }
        IT[i] = max(IT[2 * i], IT[2 * i + 1]);
    }
}

int getAns(int i, int l, int r, int x){
    if(IT[i] < x) return 0;
    if(l == r) return l;
    int m = (l + r) >> 1;
    int res = 0;
    if(IT[2 * i] >= x){
        res = getAns(2 * i, l, m, x);
    }
    if(res == 0){
        res = getAns(2 * i + 1, m + 1, r, x);
    }
    return res;
}

TranHungss(){
    FastIO();
    cin >> n >> m;
    for(int i = 1; i <= n; i++){
        cin >> a[i];
    }
    build(1, 1, n);
    for(int i = 1; i <= m; i++){
        int x; cin >> x;
        int pos = getAns(1, 1, n, x);
        cout << pos << " ";
        if(pos){
            update(1, 1, n, pos, x);
        }
    }
    return (0 ^ 0);
}

