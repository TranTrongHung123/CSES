using namespace std;

const int inf = (int)1e18;
const int maxn = 200002;

int n, a[maxn], IT[4 * maxn];

void build(int i, int l, int r){
    if(l == r){
        IT[i] = 1;
    }
    else{
        int m = (l + r) >> 1;
        build(2 * i, l, m);
        build(2 * i + 1, m + 1, r);
        IT[i] = IT[2 * i] + IT[2 * i + 1];
    }
}

void update(int i, int l, int r, int pos){
    if(l == r){
        IT[i] = 0;
    }
    else{
        int m = (l + r) >> 1;
        if(pos <= m){
            update(2 * i, l, m, pos);
        }
        else{
            update(2 * i + 1, m + 1, r, pos);
        }
        IT[i] = IT[2 * i] + IT[2 * i + 1];
    }
}

int get(int i, int l, int r, int pos){
    if(l == r) return l;
    int m = (l + r) >> 1;
    if(IT[2 * i] >= pos){
        return get(2 * i, l, m, pos);
    }
    else{
        return get(2 * i + 1, m + 1, r, pos - IT[2 * i]);
    }
}

TranHungss(){
    FastIO();
    cin >> n;
    for(int i = 1; i <= n; i++){
        cin >> a[i];
    }
    build(1, 1, n);
    for(int i = 1; i <= n; i++){
        int x; cin >> x;
        int pos = get(1, 1, n, x);
        cout << a[pos] << " ";
        update(1, 1, n, pos); 
    }
    return (0 ^ 0);
}

